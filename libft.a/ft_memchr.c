#include "libft.h"

void	*ft_memchr(const void *s, int r, size_t n)
{
	size_t	i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (i < n)
	{
		if (str[i] == (unsigned char)r)
			return ((unsigned char *)&s[i]);
		i++;
	}
	return (NULL);
}
